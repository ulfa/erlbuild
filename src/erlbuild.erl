%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
CodeReloader={code_reloader,
         	 {code_reloader, start_link, []},
              permanent,
              10000,
              worker,
              [code_reloader]},
CC_Timer={cc_timer,
         	 {cc_timer, start_link, []},
              permanent,
              10000,
              worker,
              [cc_timer]},
CC_Controller={cc_controller,
         	 {cc_controller, start_link, []},
              permanent,
              10000,
              worker,
              [cc_controller]},
CC_File_Poller={cc_file_poller,
         	 {cc_file_poller, start_link, []},
              permanent,
              10000,
              worker,
              [cc_file_poller]},
{ok, {{one_for_one, 3, 10},
		   [
			CodeReloader,
			CC_Timer,
			CC_Controller,
			CC_File_Poller
			]}}.

%% ====================================================================!
%% External functions
%% ====================================================================!
%% --------------------------------------------------------------------
%% Func: start/0
%% Returns: {ok, Pid}        |
%%          {ok, Pid, State} |
%%          {error, Reason}
%% --------------------------------------------------------------------
start() ->
	application:start(?MODULE).

start_link(_Type, _Args) ->
	supervisor:start_link({local, ?MODULE}, ?MODULE, []).
%% --------------------------------------------------------------------
%% Func: start/2
%% Returns: {ok, Pid}        |
%%          {ok, Pid, State} |
%%          {error, Reason}
%% --------------------------------------------------------------------
start(_Type, _Args) ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).
%% --------------------------------------------------------------------
%% Func: stop/0
%% Returns: any
%% --------------------------------------------------------------------
stop() ->
    application:stop(?MODULE).
%% --------------------------------------------------------------------
%% Func: stop/1
%% Returns: any
%% --------------------------------------------------------------------
stop(_State) ->
    ok.
%% --------------------------------------------------------------------
%% Func: restart/0
%% Returns: any
%% --------------------------------------------------------------------
restart() ->
    stop(),
    start().

